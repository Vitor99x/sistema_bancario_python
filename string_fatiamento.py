curso = "PyThOn"

#todas maiúsculas
print(curso.upper())

#todas minúsculas
print(curso.lower())

#titulo
print(curso.title())

#tirar os espaços do texto
print(curso.strip())

#tirar os espaços da esquerda do texto
print(curso.lstrip())

#tirar os espaços da direita do texto
print(curso.rstrip())

#centralizado com um nomo carectere, ocupando N números definido no primeiro parametro e + a adição de string
print(curso.center(10, "#"))

#adiciona o carctere com o texto - JUNÇÂO
print(".".join(curso))