nome = "Vitor Hugo Ribeiro"

#pegar do primeiro índice
print(nome[0])

#pegar do primeiro ate o índice 9
print(nome[:9])

#pegar do indicè 10 até o final
print(nome[10:])

#pegar do indicè 10 até o 16
print(nome[10:16])

#pegar do indicè 10 até o 16 e pulando de 2 em 2
print(nome[1:16:2])

#pegar todos os índices
print(nome[:])

#do ultimo índice a te o primeiro, inversão 
print(nome[::-1])

